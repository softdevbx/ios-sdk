import UIKit

extension UIFont {
    class func montserratBlack(size: CGFloat) -> UIFont {
        return UIFont(name: "Montserrat-Black", size: size)!
    }
    
    class func montserratBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Montserrat-Bold", size: size)!
    }
    
    class func montserratHairline(size: CGFloat) -> UIFont {
        return UIFont(name: "Montserrat-Hairline", size: size)!
    }
    
    class func montserratLight(size: CGFloat) -> UIFont {
        return UIFont(name: "Montserrat-Light", size: size)!
    }
    
    class func montserratRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Montserrat-Regular", size: size)!
    }
    
    class func heuristicaBold(size: CGFloat) -> UIFont {
        return UIFont(name: "Heuristica-Bold", size: size)!
    }
    
    class func heuristicaBoldItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Heuristica-BoldItalic", size: size)!
    }
    
    class func heuristicaItalic(size: CGFloat) -> UIFont {
        return UIFont(name: "Heuristica-Italic", size: size)!
    }
    
    class func heuristicaRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "Heuristica-Regular", size: size)!
    }
}
